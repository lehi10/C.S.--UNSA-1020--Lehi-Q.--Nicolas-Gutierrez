float OPERACIONES::suma(float a, float b)
    {
        return a+b;
    }
float OPERACIONES::resta(float a,float b)
    {
        return a-b;
    }
float OPERACIONES::multip(float a,float b)
    {
        return a*b;
    }
float OPERACIONES::divi(float a,float b)
    {
        return a/b;
    }
